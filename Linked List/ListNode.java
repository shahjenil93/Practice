class ListNode
{
	public int data;
	public ListNode next;

	public ListNode (int data)
	{
		this.data = data;
	}

	public void getData()
	{
		return data;
	}

	public void setData(int data)
	{
		this.data = data;
	}

	public void getNext(ListNode next)
	{
		return this.next;
	}

	public void setNext(ListNode next)
	{
		this.next = next;
	}
	

}